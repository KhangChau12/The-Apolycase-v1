// O(1) lookup map
export const BASE_SKILL_TREE_MAP = new Map<BaseSkillId, BaseSkillTreeNode>(
  BASE_SKILL_TREE.map(n => [n.id, n])
)

// All parent→child edges derived from requiresSkill
export const BASE_SKILL_TREE_EDGES: [BaseSkillId, BaseSkillId][] = BASE_SKILL_TREE
  .flatMap(node => node.requiresSkill.map(parentId => [parentId, node.id] as [BaseSkillId, BaseSkillId]))
